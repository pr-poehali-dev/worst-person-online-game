export function getRandomCondition(): string {
  return conditionCards[Math.floor(Math.random() * conditionCards.length)];
}

export function getRandomAction(): string {
  return actionCards[Math.floor(Math.random() * actionCards.length)];
}

export function getRandomCards(count: number, cards: string[]): string[] {
  const shuffled = [...cards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
